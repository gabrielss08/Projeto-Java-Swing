    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(tabelaPostos.getSelectedRow() != -1){
        
        DefaultTableModel postos = (DefaultTableModel) tabelaPostos.getModel();
        postos.removeRow(tabelaPostos.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um posto para para excluir");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(tabelaPostos.getSelectedRow() != -1){
            tabelaPostos.setValueAt(jcampoNome.getText(), tabelaPostos.getSelectedRow(), 0);
            tabelaPostos.setValueAt(jcampoEndereco.getText(), tabelaPostos.getSelectedRow(), 1);
            tabelaPostos.setValueAt(jcampoEtanol.getText(), tabelaPostos.getSelectedRow(), 2);
            tabelaPostos.setValueAt(jcampoGasolina.getText(), tabelaPostos.getSelectedRow(), 3);
            tabelaPostos.setValueAt(jcampoDiesel.getText(), tabelaPostos.getSelectedRow(), 4);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

